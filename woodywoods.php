<p>All that being said, the rest of the game doesn't have to resort to Golden Pipes and Plunder Chests. Also consider the Warp Block (or Super Warp Block should you get one) to try to swap places with a player on their way to Boo or about to buy a Golden Pipe. If you get a Chomp Call, consider using it when the star is at a desirable location. There's quite a few of them. Two put the player on the way to Boo (although one requires a Skeleton Key to get to Boo), and others are near an item shop to either get a Plunder Chest or Golden Pipe. Still, if you have enough coins, it is better to opt for Golden Pipes more often than not.</p>
<img src="images/woodywoods/goodstarspaces.png" alt="Stars Near Item Shops or Boo" width="800">
<p>And that's it for Woody Woods. It's an item-centric board where you need items like a Custom Dice Block and Cursed Dice Block to help deal with the signs. But with how interfering they are, once the Golden Pipe becomes available at shops, that becomes your main priority.</p>
