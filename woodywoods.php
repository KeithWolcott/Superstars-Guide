<li>14 <img class="smallspace" src="images/spaces/Item Space.png" alt="Item Space" /> Item Spaces</li>
<li>15 <img class="smallspace" src="images/spaces/Lucky Space.png" alt="Lucky Space" /> Lucky Spaces</li>
<li>3 <img class="smallspace" src="images/spaces/Bowser Space.png" alt="Bowser Space" /> Bowser Spaces</li>
<li>3 <img class="smallspace" src="images/spaces/VS Space.png" alt="VS Space" /> VS Spaces</li>
<li>2 <img class="smallspace" src="images/spaces/Chance Time.png" alt="Chance Time Space" /> Chance Time Spaces</li>
<li>and 3 <img class="smallspace" src="images/spaces/Bank Space.png" alt="Bank Space" /> Bank Spaces</li>
<li>for a total of 90 spaces.</li></ul>
</ul>
