<p><strong>Woody Woods</strong> is a large board made up of
<ul class="spacerundown">
<li>35 <img class="smallspace" src="images/spaces/Blue Space.png" alt="Blue Space" /> Blue Spaces</li>
<li>7 <img class="smallspace" src="images/spaces/Red Space.png" alt="Red Space" /> Red Spaces</li>
