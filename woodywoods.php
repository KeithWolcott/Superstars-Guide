<p>Back to the signs, let's say you go left. A few spaces in is Woody, which you can activate by landing on one of three spaces in front of him. You then have two fruits to choose from. One gives you between 4 and 15 coins, and the other lets you roll another dice block, giving you great mobility in one turn. Looking at the board, you can see this event is near two star spaces. If one of them is active, you can choose the coins if you are short a few coins, or choose the Dice Fruit to potentially reach the star that turn.</p>
