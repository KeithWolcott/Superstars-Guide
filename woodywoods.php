<p>Because of that, items will come in handy. The first item shop is only seven spaces away from the start. Not only is this the closest a shop has been from the start, but that means you might be able to get an item on your first turn. For the first half of the game, your options, much like Space Land, are:</p>
<ul>
<li>Skeleton Key for 3 coins</li>
<li>Cursed Dice Block for 3 coins</li>
<li>Double Dice for 5 coins</li>
<li>Warp Block for 7 coins</li>
<li>Triple Dice for 10 coins</li>
<li>and Custom Dice Block for 12 coins</li>
</ul>
</div>
<div class="content">
<p>The Cursed Dice Block, Double Dice, Triple Dice, and Custom Dice Block are all great at dealing with the sign gimmick. If you want to reach the sign while it's facing the direction you want, you can use a Double Dice or Triple Dice to make it. Most of the time, the Triple Dice is better, but keep in mind that it is possible to roll too high. You'll see that the signs after the first one aren't too far away, and it might be the case that you end up going to the next sign while it's facing the wrong way. For that reason, there are times when the Double Dice is better. The Cursed Dice Block has two usages, since you can use it on yourself or set it off on another player. If a player is approaching an arrow that is facing the right way, you can use the Cursed Dice Block on them to miss out on the path they want. You can also use it on yourself when the upcoming arrow is facing wrong, so you'll stall yourself to wait for it to switch the next turn. When it comes to doing this, you have to keep in mind the Event Space right before the junction, since landing on it will switch back to the direction you don't want to go the next turn. That's one reason why the Custom Dice Block is overall the best item to deal with the arrows. You can either use it to guarantee you roll high enough to go past the arrow while it's right, or land on the space right before the Event Space to wait for it to change the next turn. However, the item does cost 12 coins, which is the most expensive of the 4 options. Indeed, if you reach the item shop on your first turn, you won't be able to afford that, as you'll only have 10 coins.</p>
<p>When it comes to the first star, you might have to settle on a 3 coin item or no item at all to afford it. Two of the stars are right after the first sign, so you can get them on your second turn. One of them is on the left side of the sign, and the other is on the right side. At the start of the game, the signs face to the right, so it will be pointing to the left for the second turn. However, if an odd number of players land on the first Event Space (by rolling a 10), the sign will still be pointing to the right the next turn.</p>
</div>
<div class="imagefloat">
<img src="images/woodywoods/Skeleton Gate 1 Woody Woods.png" alt="Skeleton Gate 1" class="floatimage">
<p>At the start, you also have the option of looping around the beginning section. Thanks to having several blue spaces, 3 item spaces, and a Lucky Space, you can stock up on items and rack up some coins by staying over there. It also has the first Skeleton Gate, which you can use if you get a Skeleton Key from an Item Space or a Lucky Space. It's a way to get out of there, but it narrowly misses a star. Instead, it acts as a shortcut to another star. However, to get it, the sign on the far right must be facing right. From there, you'll return to the start, with another star space on the route. But the main draw is the Chance Time Space. If you have a Cursed Dice Block or a Custom Dice Block, you can use it to land on this Chance Time space if you need to turn things around.</p>
</div>
