<p>All paths eventually meet up, where you can then visit another item shop. Speaking of, when it comes to the latter half of the game, the Cursed Dice Block no longer is an option. It is instead replaced with the Golden Pipe for 25 coins. And here's where the board completely changes. As you can imagine, going for stars normally is a struggle, thanks to the board's frustating gimmick. By that point, you might have accumulated enough coins that you can pay up the steep price for the Golden Pipe. And so, for the rest of the game, the main item you (and likely everyone) will want to go for is the Golden Pipe.</p>
</div>
