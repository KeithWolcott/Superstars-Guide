<p>Another counterplay is to get a Plunder Chest yourself. As long as you have at least one other item, if the player tries to steal your Plunder Chest, they might not get it, meaning they'll no longer have a Plunder Chest while you still do. But as you can see, even then, it is possible they'll just take the Plunder Chest you just bought from you.</p>
