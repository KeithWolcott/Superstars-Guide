<p>If you instead go up, again, you'll come to the next sign, which determines whether you'll go left or right. This sign doesn't have to be as decisive as the others, due to the board's second Skeleton Gate. If you bring along a Skeleton Key, you can guarantee to be on the way to the left path. Either the arrow will be facing the right way, or it won't, but you can go through the gate. Best of all, there isn't a star space that you'll be missing out on by not going left. Instead, you'll be missing out on the next event, which might be a good thing.</p>
</div>
