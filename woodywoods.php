<p>It's possible to go back so far that you go behind the sign, which is a possible way of undoing going that path if you meant to go right. There are some issues with this. Not only do you have to roll high, but you might land on the Event Space which might put you back on the left path (if the arrow is facing left.) There's also the risk of ending up on a Bowser Space. But it is worth noting that, should you choose the Reverse-Dice Fruit, you have a pretty good chance of landing on a Lucky Space or Item Space, so you can use that to maybe get an item like a Mushroom or Double Dice to roll high enough to skip past these Event Spacest.</p>
</div>
<div class="imagefloat">
<img src="images/woodywoods/vsboo.png" alt="VS Space before Boo" class="floatimage">
<p>No matter if you go left or right, you have to go past a Koopa Bank, but the top path has an extra perk: it has Boo. Boo happens to be just a few spaces after the Koopa Bank, so you'll need a few more coins to steal a star than normal, and that's another reason against choosing the Minus Coin Fruit from Warukio. But if you are short in coins, consider the VS space right before Boo. The Koopa Bank is a double-edged sword, too, as if you land on it, you'll receive more coins to steal a star as well!</p>
</div>
