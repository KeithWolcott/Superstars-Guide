<p>After Woody is a junction that either goes to another sign or is a loop around to the first sign. The latter also has access to another item shop. This is the Forest Shop, and it has a completely different set of items. They are:</p>
<ul>
<li>Mushroom for 3 coins</li>
<li>Chomp Call for 6 coins</li>
<li>Plunder Chest for 20 coins</li>
</ul>
<p>That's right. For the first time, you can now get a Plunder Chest from an item shop! 20 coins is a rather high price, though, so it should be saved for items that are worth it, like a Boo Bell or Double Star Card.</p>
</div>
<div class="imagefloat">
