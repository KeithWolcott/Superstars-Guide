<p>Basically, if you have a Plunder Chest, players shouldn't be getting Golden Pipes, since you can just take it from them. If they do have a Golden Pipe, they immediately have to use it so you can't steal it from them, even if they won't be able to buy the star due to lack of coins. However, if the player has at least one other item, you aren't guaranteed to get their Golden Pipe, so if you have an item like a Skeleton Key, that acts as a sort of protection from players trying to steal a Golden Pipe from you.</p>
