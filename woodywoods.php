<p>In the original board from Mario Party 3, choosing nothing resulted in you getting the Dice Fruit every time. However, in this game, you'll get nothing if you don't choose in time. Thankfully, it doesn't have to be random what you get. You can pick one as they are falling, so rather than just picking a direction first, wait to see which one you want, and then go for it. You should have enough time to do that.</p>
