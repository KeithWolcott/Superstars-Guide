<p>The Forest Shop also has a new inventory, but the three old options are still available. Instead, the new option is a Hidden-Block Card for 40 coins. It's rarely a good idea to buy the Hidden-Block Card here. Sure, you can get a star, but there's always the chance of just getting a few coins, and if the latter happens, you sure aren't going to get your money back. Even if you do get the star, you're only saving 5 coins compared to getting a Golden Pipe and using it to get a star, except the star is guaranteed with the Golden Pipe. The other three options are still better, but the Plunder Chest has a great use now. It can deter people from getting a Golden Pipe.</p>
