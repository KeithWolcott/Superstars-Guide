<p>Like Yoshi's Tropical Island, the star starts at the same place every time, and Bowser is on the board as well. However, the two do not switch ever.</p>
<p>On the way to this one star, you'll come across four pairs of <img class="smallspace" src="images/spaces/Event Space.png" alt="Event Space" /> Event Spaces. Landing on them at first summons Lakitu, who gives you two options. You can either set a coin-stealing Strawberry for 5 coins, or a star-stealing Strawberry for 30 coins. This is a difference from the original, who only had the latter option.</p>
