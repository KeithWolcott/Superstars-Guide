<p>When another player lands on one of the two <img class="smallspace" src="images/spaces/Event Space.png" alt="Event Space" /> Event Spaces correlating to the Strawberry you set, they'll either have to give you between 5 to 10 coins for a small Strawberry, or a star for a big one. Afterwards, it goes away, but if it's small, there's a chance it will remain for one more steal. If the opponent doesn't have coins or a star to steal, nothing happens.</p>
