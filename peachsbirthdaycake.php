<p>And again, if you visit him in last place, there's a very small chance that he'll give you 5 coins, but again, it's rare, so usually, the previous options will occur.</p>
<p>Anyway, the fact that Bowser can give you the Cursed Dice Block is one reason why I can't recommend buying the Cursed Dice Block from the shop. The other is the fact that, well, this board has <em>NO</em> <img class="smallspace" src="images/spaces/Item Space.png" alt="Item Space" /> Item Spaces! The only other source of items are from <img class="smallspace" src="images/spaces/Lucky Space.png" alt="Lucky Space" /> Lucky Spaces, but those can just as easily give you coins, so you'll want to make the item you choose from the shop worth it.</p>
