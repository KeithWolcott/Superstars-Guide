<p>These Event Spaces take the place of Boo, who does not appear on this board. You should always set one of these traps if you have enough coins, since you will at least break even with the coin one, or you can really change up the standings by stealing a star from another player!</p>
<p>The Event Spaces being in pairs has some implications. Not only does it make them harder to avoid, it means you have the chance to activate the same one two turns in a row. You just need to stop on one and roll a 1 the next turn. If a player lands on it before your next turn, this gives you the chance to plant another one right away.</p>
