<p>A little after the board's one star is the board's one item shop. It sells the following, just like Yoshi's Tropical Island:
<ul>
<li>Mushroom for 3 coins</li>
<li>Cursed Dice Block for 3 coins</li>
<li>Double Dice for 5 coins</li>
<li>Warp Block for 7 coins</li>
<li>and Custom Dice Block for 12 coins</li>
</ul>
<p>This time, the item you choose will be based on whether you got a star or how many coins you have. If you have enough coins, you'll probably want to get back to the star as soon as possible. Thus, get a Mushroom or Double Dice. Those options can also be used to roll past a Star Stealing Piranha Plant later on in the board. If you don't have much coins, then the Warp Block can be powerful. You can use it to get out of a bad spot, but it's particularly brutal if everyone is closer to the star. Then, not only will you get a big shortcut to the star, you're impeding the progress of another player. If you didn't get a star, then get the Custom Dice Block if you can afford it. Why? Chance Time. Now, if you choose Swap Stars first, you won't have to worry about losing anything. The Event Spaces being in pairs also makes the Cursed Dice Block potent, as it can dramatically increase the chance of a player landing on one of your Piranha Plants if they are within 3 spaces, but still, I wouldn't recommend buying it.</p>
