<p>Now onto the shop in the second half of the game. Like Yoshi's Tropical Island, every item from before is still available, but this time, the new item is the Triple Dice instead of the Golden Pipe. Imagine how broken the Golden Pipe would be on this board, so it makes sense. The Triple Dice is a great choice if you have at least 50 coins and the only seed left is Bowser. Then, you can visit Bowser, only losing up to 20 coins, and buy the star afterwards.</p>
</div>
<div class="videofloat">
<p>And that's it for Peach's Birthday Cake. It stands out by not having as many items due to no Item Spaces, including having no Golden Pipes, as well as how there's no junctions outside the Flower Lottery. With the star staying in one space the entire time, it does become more important to reach it with enough coins, but if you don't, then you can hope for a player to land on a Piranha Plant that'll steal a star and give it to you, made harder by a lack of items. Otherwise, there's always Chance Time.</p>
