<p>Making matters worse is this Slide that happens just two spaces before the Item Shop. If you land on it, you'll go down to the blue space below. While this does give you an Extra <img class="smallspace" src="images/spaces/Event Space.png" alt="Event Space" />Event Space for the Eventful Bonus, this means you lose access to the Item Shop. However, it also means you avoid the Flower Lottery, so you don't have to worry about losing a lot of coins to Bowser, which sounds like a decent tradeoff, right? Well, no. And here's why.</p>
<p>In the original, going to the route with Bowser just looped you back to the Flower Lottery. However, in this board, the Bowser path doesn't go back to the Flower Lottery, but instead goes...back to the star, skipping past all those Piranha Plants! So, I would say it's actually to your advantage to get the Bowser seed, which is the opposite of the original. Even though you will be losing a lot of coins to Bowser, there's some spaces on the way that can make it worth it. There's some Lucky Spaces, where you might get enough coins to afford the star. Or there's a VS Space. If it's for enough coins and you win, there's some more coins. Right after the VS space is Chance Time, so if every player has more coins than you, just select Coin Swap and try to get yourself in it. Finally, there's a Bowser space, and while it's not a good strategy, there is the possibility of getting a Bowser Revolution. Those last three are right by each other, so if you got a Cursed Dice Block from Bowser, you can maybe use it on yourself to land on one of these spaces. But it's better to get a Custom Dice Block from the shop beforehand to guarantee you land on the best space.</p>
</div>
