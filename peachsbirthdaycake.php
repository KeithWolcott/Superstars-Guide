<li>9 <img class="smallspace" src="images/spaces/Lucky Space.png" alt="Lucky Space" /> Lucky Spaces</li>
<li>2 <img class="smallspace" src="images/spaces/Bowser Space.png" alt="Bowser Space" /> Bowser Spaces</li>
<li>2 <img class="smallspace" src="images/spaces/VS Space.png" alt="VS Space" /> VS Spaces</li>
<li>and 2 <img class="smallspace" src="images/spaces/Chance Time.png" alt="Chance Time Space" /> Chance Time Spaces</li>
<li>for a total of 51 spaces.</li></ul>
</ul>
