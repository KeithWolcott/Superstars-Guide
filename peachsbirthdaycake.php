<p>If you end up going to a normal path, there is one more flower you can take advantage of. Then, the start is not far off, where Koopa will give you 10 coins. But notice how there is a Chance Time space on the way. That means that no matter if you get Bowser or not, there will be a Chance Time space available, so if everyone but you got a star, you can get a Custom Dice Block, figure out which path you will take, and use the Custom Dice Block to land on Chance Time and aim for a Star Swap with you. And if you have less coins than everyone else, you might as well go for Star and Coin Swap.</p>
</div>
