<p>When you land on a Red Space, you lose 3 coins (or all if you have less than 3.) Nothing happens if you have 0 coins. During the last five turns, the amount you lose is increased to 6.<br>
Landing on a Red Space increases the chances of you being the solo player in a 1-vs-3 minigame. If normal bonus stars are on, there is a Bonus Star for landing on the most Red Spaces.<br>
Otherwise, this is one of the most straightforward spaces in the game.</p>
