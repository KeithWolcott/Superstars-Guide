<p>The Koopa Bank has two functions. If you pass by it, you'll have to put in 3 coins. During the last 5 turns, this amount is increased to 5. If you have less than that amount, you'll just put in all your coins. However, if you land on the space, you'll collect every coin that has been deposited in the bank. It can be 0 coins, 3 coins, or it can be a lot. Once it reaches 21 coins, you'll be able to receive more coins from the bank than from any other space. Even if you land on a blue space during the last 5 turns (to receive 6 coins) and it has a hidden block, the hidden block won't contain more than 15 coins. Once there's a good amount of coins saved up, it does become worth it to try to land on it, as long as it doesn't put you in a bad position for future turns, since the coins from this space <em>will</em> go towards the Rich Bonus. It is worth a use of the Custom Dice Block. If you instead have a Cursed Dice Block, you can increase your chances of landing on it if you're within 3 spaces by using the item on yourself.</p>
<p>If losing even 3 coins is not good for you, the banks are avoidable, but sometimes, you will have to pass them to get a star. As such, if you are going to use an item like a Triple Dice to get a star, you need to make sure you will afford the star if a Koopa Bank is on its path. If you have between 20 and 22 coins, you won't have enough coins to buy the star.</p>
<p>Note that this space does not appear in the Mario Party boards (Yoshi's Tropical Island and Peach's Birthday Cake.) Instead, Koopa gives 10 coins to players who pass by him, where he's positioned at the start of the board.</p>
