<p>When you land on a VS Space, a VS minigame will activate. First, the player who landed on the space will get 3 coins, no matter what turn it is or what placement the player is in. Then, the amount of coins every player will contribute is decided. The options are 5, 7, 10, 15, 20, 25, and 30. However, only 4 options will show up on the roulette.</p>
