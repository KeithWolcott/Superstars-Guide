<h3>Hidden Blocks</h3>
<div class="onevideo">
<video width="540" height="360" autoplay muted loop>
  <source src="videos/spaces/HiddenBlockWithCoins.mp4" type="video/mp4">
</video>
<p>There is always a Hidden Block placed on a random Blue Space somewhere on the board. Should you land on the Blue Space, your character will look around before a block spawns above. Hit it, and you'll receive some coins. You'll receive between 4 and 15 coins. The lower your placement, the more coins you'll receive. Afterwards, the hidden block is moved to another blue space.</p></div>
<div class="onevideo">
<video width="540" height="360" autoplay muted loop>
  <source src="videos/spaces/HiddenBlockWithStar.mp4" type="video/mp4">
</video>
<p>There is also a chance that the Hidden Block will contain a star instead of coins. You won't know until you hit the block. This can happen regardless of turn or placement, but it generally happens in the latter half for lower placements. Still, this is why hidden blocks can really turn the tide of a match.<br>
One option you have in a match is to land on a blue space in the hope that it contains a hidden block, preferably with a star. If the game goes on long enough without a hidden block found, then you can target blue spaces that haven't been landed on. Don't bet on a blue space having a hidden block, though. After all, there's only one. Thus, it's not such a good idea to use a Custom Dice Block to solely land on a blue space just for he hope that it has a hidden block.</p>
