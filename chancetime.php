<p>For the other two dice blocks, the order of the players is based on the turn order. For example, if the turn order is Rosalina, Yoshi, Waluigi, and Donkey Kong, and Waluigi landed on the Chance Time space, the dice blocks will cycle through Waluigi, Donkey Kong, Rosalina, Yoshi.</p>
