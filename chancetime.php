<p>When you land on a Chance Time Space, you activate the Chance Time event, where players can exchange coins and stars. As you can imagine, Chance Time has the possibility of completing changing the outcome of the game, and this is the reason why you can turn things around if you get unlucky. What makes this event stand out is how you actually have some control of the outcome. You get the decide the order of the three blocks, two of which determine the players involved and the middle being the items being traded. Each time you hit a dice block, the other ones spin faster, meaning the only one where you really get to choose is the first one.</p>
